let par = document.getElementById("PAR");


function ChangeFont(fontType) {
  return par.style.fontFamily = fontType;
}

function ChangeAlign(alignType) {
  return par.style.textAlign = alignType;
}

function ChangeHeight(height) {
  return par.style.lineHeight = height;
}

function ChangeLSpace(letterSpace) {
  return par.style.letterSpacing = letterSpace;
}

function ChangeIndent(indent) {
  return par.style.textIndent = indent;
}

function ChangeTransform(format) {
  return par.style.textTransform = format;
}
function ChangeDecorate(decorate) {
  return par.style.textDecoration = decorate;
}

function ChangeBorder(border) {
  return par.style.border = border;
}

function ChangeBorderColor(color) {
  return par.style.borderColor = color;
}
